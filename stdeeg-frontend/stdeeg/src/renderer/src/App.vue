<template>
  <router-view></router-view>
</template>

<script>
export default {
  name: 'App',
  created() {
    window.addEventListener("keyup", this.keyUp, false);
  },
  methods: {
    keyUp(e) {
      if(e.code === 'F1') {
        if(this.$router.currentRoute.value.path === '/') {
          this.$router.push('/about');
        } else if(this.$router.currentRoute.value.path === '/about') {
          this.$router.push('/');
        }
      }
    },
  }
}
</script>
